public class Dog {
	
	public void bark() {
		System.out.println("the dog is barking");
	}
	
	public void bark(String str) {
		System.out.println("the dog is howling");
	}
	
	public void bark(Boolean bool) {
		System.out.println("the dog is woofing");
	}
	
	public void bark(Integer integ) {
		System.out.println("the dog is roaring");
	}
	
	public void bark(Double dbl) {
		System.out.println("the dog is squeeking loudly");
	}
	

}
